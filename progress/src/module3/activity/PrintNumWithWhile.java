public class PrintNumWithWhile {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		
		// Print all even numbers less than 100
		int i = 0;
		System.out.println("Printing all even numbers");
		while(i<100){
			System.out.println(i);
			i=i+2;			
		}
		
		 
		// write code to Print all odd numbers less than 100
		int j = 1;
		System.out.println("Printing all odd numbers");
		while(j<100){
			System.out.println(j);
			j=j+2;
		}


	}

}
